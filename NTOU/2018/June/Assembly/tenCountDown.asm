ORG 00H
JMP START
ORG 50H
START: MOV A,#10
MOV DPTR,#TABLE
LOOP:
SHOW:
PUSH ACC
DEC A
MOVC A,@A+DPTR
MOV P1,A
ACALL DELAY
POP ACC
DJNZ ACC,SHOW
JMP START

DELAY: MOV R2,#10
D1: MOV R0,#250
D2: MOV R1,#250
DJNZ R1,$
DJNZ R0,D2
DJNZ R2,D1
RET
TABLE: DB 11000000B,11111001B,10100100B,10110000B ;O-3
DB 10011001B,10010010B,10000010B,11111000B ;4-7
DB 10000000B,10010000B,10001000B,10000011B ;8-C
DB 11000110B,10100001B,10000110B,10001110B ;D-F
END